# Python script to calculate the square and cube of a given number

# Step 1: Get the number from the user
number = float(input("Enter a number: "))

# Step 2: Calculate the square of the number
square = number ** 2

# Step 3: Calculate the cube of the number
cube = number ** 3

# Step 4: Display the results
print("The square of", number, "is:", square)
print("The cube of", number, "is:", cube)
